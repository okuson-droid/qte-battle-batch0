package com.example.qte.game;

/**
 * ミニオンが破壊された原因。
 *
 * 「戦闘で破壊された時」(ボーン・コレクター)のように、破壊の原因を条件にするカードが
 * 闇文明で初めて登場したため導入した。破壊そのものは同じ処理でも、
 * 誘発するトリガーが異なるため、原因を破壊処理の引数として持ち回る。
 */
public enum DestructionCause {

    /** 戦闘(ミニオン同士の殴り合い・リーダーのウェポン攻撃)による破壊 */
    COMBAT,

    /** カード効果による破壊(効果ダメージによるHP0を含む) */
    EFFECT
}

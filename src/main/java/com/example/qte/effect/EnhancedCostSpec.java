package com.example.qte.effect;

/**
 * 「このカードのコストを+Nしてもよい。そうした場合〜」という追加コストの宣言(a5)。
 *
 * <b>既存の「してもよい」との違い。</b>
 * これまでの任意効果はすべて対象選択の任意化({@code Requirement.optional})で表現されていた。
 * つまり「選ぶか選ばないか」であり、選ばなければ効果が起きないだけで支払う額は変わらない。
 * 風文明の2枚(回帰の風穴・風弾の跳弾)は、対象を伴わずに支払う額そのものが変わる
 * <b>二者択一</b>であり、既存の枠組みでは表現できない。
 *
 * <b>なぜ割り込み選択(a9)に載せないのか。</b>
 * コストに影響する選択は支払いより前に確定していなければならない。
 * a9 は「効果の解決の途中で問い合わせる」仕組みであり、支払いはとっくに終わっている。
 * 性質が異なるため、こちらは使用宣言に付随する真偽値
 * ({@code PlayCardRequest.enhanced})として通信経路に載せる。
 *
 * @param extraCost 追加で支払うコスト
 * @param prompt    クライアントの確認ダイアログに出す文言
 */
public record EnhancedCostSpec(int extraCost, String prompt) {
}
